    private void jButtonOriginalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOriginalActionPerformed
        //Variable and object declaration
        double chickenprice = 1.00;
        double thigh = 8;
        double breast = 6;
        double set = 3;
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        //if else statement to check customer order choices
        if (jRadioButtonTHIGH.isSelected() && jRadioButtonSET.isSelected()){
            chickenprice += (thigh + set);   
            table.addRow(new Object[]{"Original Chicken, Thigh, Set", "1", String.format("%.2f", chickenprice)});
        }
        else if (jRadioButtonTHIGH.isSelected() && jRadioButtonALACARTE.isSelected()){
            chickenprice += thigh;
            table.addRow(new Object[]{"Original Chicken, Thigh, Ala Carte ", "1", String.format("%.2f", chickenprice)});
        }
        if (jRadioButtonBREAST.isSelected() && jRadioButtonSET.isSelected()){
            chickenprice += (breast + set);
            table.addRow(new Object[]{"Original Chicken, Breast, Set", "1", String.format("%.2f", chickenprice)});
        }
        else if (jRadioButtonBREAST.isSelected() && jRadioButtonALACARTE.isSelected()){
            chickenprice += breast;
            table.addRow(new Object[]{"Original Chicken, Breast, Ala Carte ", "1", String.format("%.2f", chickenprice)});
        }
        Price();
    }//GEN-LAST:event_jButtonOriginalActionPerformed

