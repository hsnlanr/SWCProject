    private double FinalTotal;
    //Function Price to calculate customer price based on their choices
    public void Price(){
        double donation = 0;
        //if else statement to check if customers picked to donate
        if (jRadioButtonDRM1.isSelected()){
            donation = 1;
        }
        else if (jRadioButtonDRM5.isSelected()){
            donation = 5;
        }
        else if (jRadioButtonDRM10.isSelected()){
            donation = 10;
        }
        else if (jRadioButtonDRM15.isSelected()){
            donation = 15;
        }
        double total = 0;
        //for loop to get item price from jtable
        for (int i = 0; i<jTable1.getRowCount(); i++){
            total += Double.parseDouble(jTable1.getValueAt(i,2).toString());
        }
        //Updating subtotal, donation, total  textfields to current values after choices
        jTextFieldSubTotal.setText(Double.toString(total));
        double ctotal = Double.parseDouble(jTextFieldSubTotal.getText());
        String cDonation = String.format("%.2f", donation);
        jTextFieldDonation.setText(cDonation);
        String cTotal = String.format("%.2f", ctotal);
        jTextFieldSubTotal.setText(cTotal);
        FinalTotal = ctotal + donation;
        String cFinalTotal = String.format("%.2f", FinalTotal);
        jTextFieldTotal.setText(cFinalTotal);
    }
